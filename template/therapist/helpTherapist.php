    <td  class="helpsubheading"  >Create a New Template Plan for Your Personal Library<a name="createnewtemplate" id="createnewtemplate"></a></td>
    <td valign="bottom">&nbsp;</td>
  </tr>
  <tr>
    <td colspan="2">&nbsp;</td>
    <td   ><p>Definition: Template plans are standard treatment plans that have  been created for particular diagnoses, not for a particular patient. Template  plans can be assigned to a patient with no additional customization or can be  customized for a particular patient and then assigned. The effective use of  template plans can save you time versus creating a treatment plan from scratch  for each patient.</p>
      <p>Your personal library will contain  template plans when you first login. These are template plans Tx Xchange has  created for you. You can assign these plans to your patients, modify them as  you see fit, or delete them if they are not beneficial to your practice.</p>
      <ol start="1" type="1">
        <li>Click <u>My       Tx Plans</u> link to go to your personal library of template plans.</li>
        <li>Click       &quot;Create New Template Plan&quot; button.</li>
        <li>Give       the template plan a name (e.g. Sprained ACL). Please note this is a       required field to move forward with the process.</li>
        <li>Click       &quot;Next&quot; button.</li>
        <li>You will now choose which treatments you want in  the plan. To search the Tx Xchange database for exercises you want, put in key  terms such as a muscle, condition, body part, equipment, etc. If you get too  many results, type in another term to narrow the results. (Separate terms with  commas). If you don’t get the results you are looking for, type in a different  term.</li>
        <li>&quot;Drag and drop&quot; the individual  pictures down into the &quot;Treatments in Plan&quot; section of the page.  Change search criteria as necessary.</li>
        <li>You can change the order of the individual  &quot;Treatments in Plan&quot; by dragging and dropping them into the different  numbered positions.</li>
        <li>If you want to remove a treatment from the plan,  drag it into the &quot;trash&quot;.</li>
        <li>Click       &quot;Next&quot; button.</li>
        <li>The next step customizing the instructions  associated with each individual treatment. Choose &quot;Edit Instructions&quot;  from the &quot;Actions&quot; menu. Make the changes you want to any of the  fields. Click &quot;Save Custom Instructions&quot; button.</li>
        <li>Designate “Tags” to use for future searches and  add the template plan to your “Favorites” by choosing “Add Tags/Favorites” from  the “Action” menu.</li>
        <li>Click &quot;Next&quot; button.</li>
        <li>The next step is adding articles (i.e. patient  education information) to the plan. To preview the article first before adding  to the plan, select &quot;Preview Article&quot; from the &quot;Actions&quot;  menu. To add an article to the plan, choose &quot;Add Article&quot; from the  &quot;Actions&quot; menu. Newly added articles will appear at the top of the  page. To remove an article from the plan, choose &quot;Remove Article&quot;  from the &quot;Actions&quot; menu.</li>
        <li>Click &quot;Save as Template&quot; button.</li>
      </ol></td>
