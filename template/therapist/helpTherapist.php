    <td  ><ol start="1" type="1"><li>	Go to a Patient Record either by clicking on My Patients link on the left side of your home page; clicking on a Patient Name link; or using the "Patient Quick Search" field.</li>
        <li>From the Patient record page, click the green &quot;Create  New Treatment Plan&quot; button.</li>
        <li>Give the plan a name. Please note this is a  required field to move forward with the process.</li>
        <li>Click &quot;Next&quot; button.</li>
        <li>You will now choose which treatments you want in  the plan. To search the Tx Xchange database for exercises you want, put in key  terms such as a muscle, condition, body part, equipment, etc. If you get too  many results, type in another term to narrow the results. (Separate terms with  commas). If you don’t get the results you are looking for, type in a different  term.</li>
        <li>&quot;Drag and drop&quot; the individual  pictures down into the &quot;Treatments in Plan&quot; section of the page.  Change search criteria as necessary.</li>
        <li>You can change the order of the individual  &quot;Treatments in Plan&quot; by dragging and dropping them into the different  numbered positions.</li>
        <li>If you want to remove a treatment from the plan,  drag it into the &quot;trash&quot;.</li>
        <li>Click &quot;Next&quot; button.</li>
        <li>To customize the instructions associated with  each individual treatment, choose &quot;Edit Instructions&quot; from the  &quot;Actions&quot; menu. Make the changes you want to any of the fields. Click  &quot;Save Custom Instructions&quot; button.</li>
        <li>Click &quot;Next&quot; button.</li>
        <li>The next step is adding articles (i.e.  health/wellness information) to the plan. To preview the article first before  adding to the plan, select &quot;Preview Article&quot; from the  &quot;Actions&quot; menu. To add an article to the plan, choose &quot;Add  Article&quot; from the &quot;Actions&quot; menu. Newly added articles will  appear at the top of the page. To remove an article from the plan, select  &quot;Remove Article&quot; from the &quot;Actions&quot; menu.</li>
        <li>Click &quot;Next&quot; button.</li>
        <li>Click the &quot;Assign &amp; Notify&quot; button  to notify your patient of their new plan; click &quot;Assign w/o  Notification&quot; to assign them the plan, but not notify them&quot;; or click  &quot;Preview&quot; to review the plan with your patient before assigning.</li>
    </ol></td>
