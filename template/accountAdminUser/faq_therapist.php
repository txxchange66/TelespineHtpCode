  </tr>
  <tr>
    <td colspan="2">&nbsp;</td>
    <td valign="top" style="padding-left:50px;"><p>With TX's referral marketing service your patients or clients can much more effectively refer a friend, family member, or co-worker to your clinic, studio, of fitness facility and increase new customer volume for your business. While word-of-mouth referrals from existing patients or clients are likely an important source of new customers, most of them never end up on your schedule. Why? Because information about your business is usually forgotten, misplaced, or never looked up. Tx Xchange's referral marketing service solves this problem by making sure all the information necessary to schedule an appointment with your business is emailed to the person being referred. Your business' key information won't be easily forgotten or misplaced any longer. The service leverages the idea that the most effective form of marketing is recommendation-based where both parties know each other. Think of TX's referral marketing service as a significantly better "mousetrap" for word-of-mouth referrals.</p></td>
  </tr>
