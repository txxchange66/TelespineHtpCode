  </tr>
  <tr>
    <td colspan="2">&nbsp;</td>
    <td valign="top" style="padding-left:50px;">
      <p>If you enable the service, your patients or clients will see a well-designed "call-to-action" button on their homepage (see screen shot below). Every time they login, they'll see the "call-to-action" asking them to refer people they know to your practice or business. When they click on it, a special window opens (see screen shot below) and the patient fills in some basic information about the person they want to refer and writes a personal note. They click "Send" and the personal note along with your business' telephone number, email address, website, mapped address, business overview, and logo are automatically emailed to your potential new patient or client.</p> </td>
  </tr>
