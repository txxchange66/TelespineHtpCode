  </tr>
   
  <tr>
    <td colspan="2">&nbsp;</td>
    <td valign="top" style="padding-left:50px;"><p>The service costs just $1.99 each time one of your patients or clients clicks "Send" to email a personal note and your business' key information to someone they know and want to refer. If the service is enabled, but your patients do not use it to refer your business or clinic, there is no cost. </p>
	<p>Once the service is enabled, there is nothing you or your staff needs to do other than use Tx Xchange with all of your patients and clients and help them achieve the outcomes they want. They'll do the rest.</p>
