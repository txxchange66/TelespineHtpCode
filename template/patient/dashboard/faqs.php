									<h2 id="FunctionalScore">Q: What is a functional score?</span></h2>
                                    <div class="date" style="padding-left:12px;">Last Updated: May 22,2014</div>
									
                                    <br>
                                    <p><b>A:</b> Functional scores are generated by the Oswestry Survey you will be taking 4 times (every 2 weeks) through the program. Functional scores are used to measure the extent of your activity limitations and pain levels. Please keep in mind that lower scores are better than higher ones. Subsequently, we want to see your score go down over the course of the program. That’s how we objectively measure your progress.</p>
                                    
